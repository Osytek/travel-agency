import {connect} from 'react-redux';
import TripListOptions from './TripListOptions';
import {getAllTags} from '../../../redux/tagsRedux';
import {getAllFilters, changeSearchPhrase, changeDurationValue, changeBoxCheck} from '../../../redux/filtersRedux';

const mapStateToProps = state => ({
  tags: getAllTags(state),
  filters: getAllFilters(state),
});

const mapDispatchToProps = dispatch => ({
  changeSearchPhrase: phrase => dispatch(changeSearchPhrase(phrase)),
  // TODO - add more dispatchers for other filters
  changeDurationValue: value => dispatch(changeDurationValue(value)),
  changeBoxCheck: checked => dispatch(changeBoxCheck(checked)),
});

export default connect(mapStateToProps, mapDispatchToProps)(TripListOptions);
